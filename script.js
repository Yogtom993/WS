function formatPrice(value) {
  return value.toLocaleString("uk-UA") + " грн";
}

function createCard(product) {
  return `
    <article class="product-card" data-category="${product.category}" data-price="${product.price}" data-discount="${product.discount}" data-study="${product.study}">
      <img src="images/${product.image}" alt="${product.name}">
      <span class="badge">${product.categoryUa}</span>
      <h3>${product.name}</h3>
      <p>${product.desc}</p>
      <div class="price-row"><strong>${formatPrice(product.price)}</strong><a href="contacts.html" class="buy-btn">Купити</a></div>
    </article>
  `;
}

function renderProducts(list, limit = null) {
  const grid = document.querySelector(".catalog-products, .home-products");
  if (!grid) return;
  const data = limit ? list.slice(0, limit) : list;
  grid.innerHTML = data.map(createCard).join("");
}

document.addEventListener("DOMContentLoaded", function () {
  const isHome = document.querySelector(".home-products");
  const isCatalog = document.querySelector(".catalog-products");

  if (isHome) {
    renderProducts(PRODUCTS, 24);
  }

  if (isCatalog) {
    renderProducts(PRODUCTS);
    const buttons = Array.from(document.querySelectorAll(".filter-link"));
    const discount = document.getElementById("discountFilter");
    const budget = document.getElementById("budgetFilter");
    const study = document.getElementById("studyFilter");
    const reset = document.getElementById("resetFilters");
    const status = document.getElementById("filterStatus");
    const searchInput = document.querySelector(".search input[name='q']");
    let activeCategory = new URLSearchParams(location.search).get("category") || "all";
    const q = new URLSearchParams(location.search).get("q") || "";
    if (searchInput) searchInput.value = q;

    function applyFilters() {
      const query = searchInput ? searchInput.value.toLowerCase().trim() : "";
      const filtered = PRODUCTS.filter(product => {
        const categoryOk = activeCategory === "all" || product.category === activeCategory;
        const searchOk = !query || product.name.toLowerCase().includes(query) || product.desc.toLowerCase().includes(query) || product.categoryUa.toLowerCase().includes(query);
        const discountOk = !discount.checked || product.discount;
        const budgetOk = !budget.checked || product.price <= 15000;
        const studyOk = !study.checked || product.study;
        return categoryOk && searchOk && discountOk && budgetOk && studyOk;
      });
      renderProducts(filtered);
      buttons.forEach(btn => btn.classList.toggle("active", btn.dataset.category === activeCategory));
      status.textContent = filtered.length ? "Показано товарів: " + filtered.length : "За вибраними фільтрами товарів не знайдено.";
    }

    buttons.forEach(btn => btn.addEventListener("click", () => {
      activeCategory = btn.dataset.category;
      applyFilters();
    }));
    [discount, budget, study].forEach(el => el.addEventListener("change", applyFilters));
    if (searchInput) searchInput.addEventListener("input", applyFilters);
    reset.addEventListener("click", () => {
      activeCategory = "all";
      discount.checked = false;
      budget.checked = false;
      study.checked = false;
      if (searchInput) searchInput.value = "";
      applyFilters();
    });
    applyFilters();
  }

  const productSelect = document.querySelector("select[name='product']");
  if (productSelect) {
    productSelect.innerHTML = PRODUCTS.map(p => `<option>${p.name} — ${formatPrice(p.price)}</option>`).join("");
  }
});
